package org.example.Kotlin.learn.task2

fun main(args : Array<String>) {
    var a_float : Float = 32.32F
    var a_double : Double = 64.64

    var a_byte : Byte = 8
    var a_short : Short = 16
    var a_int : Int = 32
    var a_long : Long = 64

    var a_string : String = "string"
    var a_boolean : Boolean = false
    var a_char : Char = 'a'

    println("Decimal:")
    println("Double : ${a_double}")
    println("Float : ${a_float}\n")
    println("Integer: ")
    println("Byte : ${a_byte}")
    println("Short : ${a_short}")
    println("Int : ${a_int}")
    println("Long : ${a_long}\n")
    println("String:")
    println("String : ${a_string}")
    println("Character : ${a_char}")
    println("Boolean : ${a_boolean}")
}